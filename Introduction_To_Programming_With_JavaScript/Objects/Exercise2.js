// Which of the following values are valid keys for an object?

1
// valid
'1'
// valid
undefined
// valid
'hello world'
// valid
true
// valid
'true'
// valid
// All JS keys are strings, if a non-string is attached to an object as a key the engine will coerce
// value to be a string behind the scenes, in this example 1 and '1' represent the same key so be 
// AWARE!!